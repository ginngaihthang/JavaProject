package application.entry;

public class Users {

	private int Lib_id;
	private String Lib_email;
	private String Lib_password;
	public String Lib_name;
	public int getLib_id() {
		return Lib_id;
	}
	public void setLib_id(int lib_id) {
		Lib_id = lib_id;
	}
	public String getLib_email() {
		return Lib_email;
	}
	public void setLib_email(String lib_email) {
		Lib_email = lib_email;
	}
	public String getLib_password() {
		return Lib_password;
	}
	public void setLib_password(String lib_password) {
		Lib_password = lib_password;
	}
	public String getLib_name() {
		return Lib_name;
	}
	public void setLib_name(String lib_name) {
		Lib_name = lib_name;
	}
	
	
}
